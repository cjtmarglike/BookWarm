public class PublisherBoardVO {
	private int publisher_no;
	private String publisher_name;
	private int publilsher_phone;
	private String publisher_addr;
	
	public int getPublisher_no() {
		return publisher_no;
	}
	public void setPublisher_no(int publisher_no) {
		this.publisher_no = publisher_no;
	}
	public String getPublisher_name() {
		return publisher_name;
	}
	public void setPublisher_name(String publisher_name) {
		this.publisher_name = publisher_name;
	}
	public int getPublilsher_phone() {
		return publilsher_phone;
	}
	public void setPublilsher_phone(int publilsher_phone) {
		this.publilsher_phone = publilsher_phone;
	}
	public String getPublisher_addr() {
		return publisher_addr;
	}
	public void setPublisher_addr(String publisher_addr) {
		this.publisher_addr = publisher_addr;
	}
	
	
}
